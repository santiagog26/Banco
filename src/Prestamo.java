public class Prestamo {
    double cantidad;
    int noCuotas;
    double interes;
    double valorCuota;

    public Prestamo(double cantidad, int noCuotas, double interes, double valorCuota) {
        this.cantidad = cantidad;
        this.noCuotas = noCuotas;
        this.interes = interes;
        this.valorCuota = valorCuota;
    }
}

